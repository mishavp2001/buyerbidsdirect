import * as React from 'react';
import { Icon, useAuthenticator } from '@aws-amplify/ui-react';
import { Link } from 'react-router-dom';
import Box from '@mui/material/Box';
import Avatar from '@mui/material/Avatar';
import Menu from '@mui/material/Menu';
import MenuItem from '@mui/material/MenuItem';
import ListItemIcon from '@mui/material/ListItemIcon';
import Divider from '@mui/material/Divider';
import IconButton from '@mui/material/IconButton';
import Tooltip from '@mui/material/Tooltip';
import { Logout, House } from '@mui/icons-material/index';

export default function AccountMenu() {
    const { user, authStatus, signOut } = useAuthenticator((context) => [
        context.authStatus,
        context.signOut
    ]);

    function handleLogOut() {
        signOut();
    }

    const [anchorEl, setAnchorEl] = React.useState<null | HTMLElement>(null);
    const open = Boolean(anchorEl);
    const handleClick = (event: React.MouseEvent<HTMLElement>) => {
        setAnchorEl(event.currentTarget);
    };
    const handleClose = () => {
        setAnchorEl(null);
    };
    return (
        <React.Fragment>
            <Box sx={{ display: 'flex', alignItems: 'center', textAlign: 'center' }}>
                {authStatus !== 'authenticated' ?
                    <Link to='/login'>Login</Link> :
                    <Tooltip title="Account settings">
                        <IconButton
                            onClick={handleClick}
                            size="small"
                            sx={{ ml: 2 }}
                            aria-controls={open ? 'account-menu' : undefined}
                            aria-haspopup="true"
                            aria-expanded={open ? 'true' : undefined}
                        >
                            <Avatar sx={{ width: 32, height: 32 }}>{user?.signInDetails?.loginId?.[0]}</Avatar>
                        </IconButton>
                    </Tooltip>
                }
            </Box>
            <Menu
                anchorEl={anchorEl}
                id="account-menu"
                open={open}
                onClose={handleClose}
                onClick={handleClose}
                PaperProps={{
                    elevation: 0,
                    sx: {
                        overflow: 'visible',
                        backgroundColor: 'white',
                        filter: 'drop-shadow(0px 2px 8px rgba(0,0,0,0.32))',
                        mt: 1.5,
                        '& .MuiAvatar-root': {
                            width: 32,
                            height: 32,
                            ml: -0.5,
                            mr: 1,
                        },
                        '&::before': {
                            content: '""',
                            display: 'block',
                            position: 'absolute',
                            top: 0,
                            right: 14,
                            width: 10,
                            height: 10,
                            transform: 'translateY(-50%) rotate(45deg)',
                            zIndex: 0,
                        },
                    },
                }}
                transformOrigin={{ horizontal: 'right', vertical: 'top' }}
                anchorOrigin={{ horizontal: 'right', vertical: 'bottom' }}
            >
                <MenuItem onClick={handleClose}>
                    <Avatar /><Link to="/profile">{user?.signInDetails?.loginId}</Link>
                </MenuItem>
                <MenuItem onClick={handleClose}>
                    <ListItemIcon>
                        <Icon
                            ariaLabel="Flag"
                            pathData="M14.4 6L14 4H5v17h2v-7h5.6l.4 2h7V6z"
                            color="rebeccapurple" />                </ListItemIcon>
                    <Link to="/offers" style={{ marginRight: '2em', color: 'inherit', textDecoration: 'none' }}>
                        Offers
                    </Link>

                </MenuItem>
                <MenuItem onClick={handleClose}>
                    <ListItemIcon>
                        <House />
                    </ListItemIcon>

                    <Link to="/sales" style={{ marginRight: '2em', color: 'inherit', textDecoration: 'none' }}>
                        Properties
                    </Link>
                </MenuItem>
                <Divider />
                <MenuItem onClick={handleClose}>
                    <ListItemIcon>
                        <Logout fontSize="small" />
                    </ListItemIcon>
                    <Link to='/' onClick={handleLogOut}>Logout</Link>
                </MenuItem>
            </Menu>
        </React.Fragment>
    );
}
